    <h1>Linear Systems With Two Variables</h1>
    <p>
        A linear system of two equations with two variables is any system that can be written in the form.
        <br>
        <p class="equation">ax+by=p<br>
        cx+dy=q</p>
        where any of the constants can be zero with the exception that each equation must have at least one variable in it.<br><br>
        Also, the system is called linear if the variables are only to the first power, are only in the numerator and there are no products of variables in any of the equations.<br><br>
        Here is an example of a system with numbers.<br>
        <p class="equation"> 3x-y=7<br>
            2x+3y=1
        </p>
        Before we discuss how to solve systems, we should first talk about just what a solution to a system of equations is. A solution to a system of equations is a value of x and a value of y that, when substituted into the equations, satisfies both equations at the same time.<BR><BR>
        For the example above x=2 and y=−1 is a solution to the system. This is easy enough to check.<BR>
        <p class="equation">3(2)-(-1)=7<br>
        2(2)+3(-1)=1</p>
        So, sure enough that pair of numbers is a solution to the system. Do not worry about how we got these values. This will be the very first system that we solve when we get into examples.<br><br>
        Note that it is important that the pair of numbers satisfy both equations. For instance, x=1 and y=−4 will satisfy the first equation, but not the second and so isn’t a solution to the system. Likewise, x=−1 and y=1 will satisfy the second equation but not the first and so can’t be a solution to the system.<br><br>
    </p>
