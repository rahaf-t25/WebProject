        <p>
            We’ll start off the solving portion of this chapter by solving linear equations. A linear equation is any equation that can be written in the form<br><br>
            <p class="equation">ax+b=0</p>
            where a and b are real numbers and x is a variable. This form is sometimes called the standard form of a linear equation. Note that most linear equations will not start off in this form. Also, the variable may or may not be an x so don’t get too locked into always seeing an x there.<Br><br>
                <hr><br>
                To solve linear equations we will make heavy use of the following facts.<br><br>

                <ol>
                    <li>If a=b=b then a+c= b+c for any c. All this is saying is that we can add a number, c, to both sides of the equation and not change the equation.</li>
                    <li>If a = b then a - c = b-c for any c. As with thet last property we cam substract a number,c, from both sides of an equation. </li>
                    <li>If a=b then ac=bc for any c. Like addition and subtraction, we can multiply both sides of an equation by a number, c, without changing the equation.</li>
                </ol>
                <hr><br>
                These facts form the basis of almost all the solving techniques that we’ll be looking at in this chapter so it’s very important that you know them and don’t forget about them. One way to think of these rules is the following. What we do to one side of an equation we have to do to the other side of the equation. If you remember that then you will always get these facts correct.<br><br>
                In this section we will be solving linear equations and there is a nice simple process for solving linear equations. Let’s first summarize the process and then we will work some examples.<br><br>
                <hr>
                <h4>Process for solving linear equations</h4>
                <ol>
                    <li>If the equation contains any fractions use the least common denominator to clear the fractions. We will do this by multiplying both sides of the equation by the LCD.Also, if there are variables in the denominators of the fractions identify values of the variable which will give division by zero as we will need to avoid these values in our solution.</li><br>
                    <li>Simplify both sides of the equation. This means clearing out any parenthesis and combining like terms.</li><br>
                    <li>Use the first two facts above to get all terms with the variable in them on one side of the equations (combining into a single term of course) and all constants on the other side.</li><br>
                    <li>If the coefficient of the variable is not a one use the third or fourth fact above (this will depend on just what the number is) to make the coefficient a one.Note that we usually just divide both sides of the equation by the coefficient if it is an integer or multiply both sides of the equation by the reciprocal of the coefficient if it is a fraction.</li><br>
                    <li>VERIFY YOUR ANSWER! This is the final step and the most often skipped step, yet it is probably the most important step in the process. With this step you can know whether or not you got the correct answer long before your instructor ever looks at it. We verify the answer by plugging the results from the previous steps into the original equation. It is very important to plug into the original equation since you may have made a mistake in the very first step that led you to an incorrect answer. Also, if there were fractions in the problem and there were values of the variable that give division by zero (recall the first step…) it is important to make sure that one of these values didn’t end up in the solution set. It is possible, as we’ll see in an example, to have these values show up in the solution set.

                    </li><br>
                </ol>
